<?php
$servername = "localhost";
$username = "root";
$password = "root";
$dbname = "T2311E_LastAssignment";

$conn = new mysqli($servername, $username, $password, $dbname);

if ($conn->connect_error) {
    die("Connection failed: " . $conn->connect_error);
}

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $name = $_POST["name"];
    $phoneNumber = $_POST["phoneNumber"];

    $stmt = $conn->prepare("INSERT INTO PhoneBook (Name, PhoneNumber) VALUES (?, ?)");
    $stmt->bind_param("ss", $name, $phoneNumber);

    if ($stmt->execute()) {
        echo "<script>alert('Contact added successfully!'); window.location.href='index.php';</script>";
    } else {
        echo "Error: " . $stmt->error;
    }

    $stmt->close();
}

$conn->close();
